def aumentar(a,b=10):
    porcentagem = (a*b)/100
    porcentagem = porcentagem + a
    return porcentagem

def diminuir (a,b=10):
    porcentagem = a*b/100
    porcentagem = a - porcentagem
    return porcentagem

def dobro(a):
    aumento = a*2
    return aumento

def metade(a):
    meio = a/2
    return meio